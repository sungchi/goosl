export function normalizeLanguage(value) {
  const primary = String(value ?? "").trim().toLowerCase().split(/[-_]/)[0];
  return SUPPORTED_LANGUAGES.includes(primary) ? primary : null;
}

export function detectLanguage({ languages = [], language } = {}) {
  const preferredLanguage = languages[0] ?? language;
  return normalizeLanguage(preferredLanguage) === "ko" ? "ko" : DEFAULT_LANGUAGE;
}

export function translateText(language, key, variables = {}) {
  const locale = normalizeLanguage(language) ?? DEFAULT_LANGUAGE;
  const template = TRANSLATIONS[locale]?.[key] ?? TRANSLATIONS[DEFAULT_LANGUAGE]?.[key] ?? key;
  return String(template).replace(
    /\{([A-Za-z0-9_]+)\}/g,
    (_, name) => String(variables[name] ?? `{${name}}`),
  );
}
